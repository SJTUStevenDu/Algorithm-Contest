bool check(int x)
{
    int y=0,l=0;
    while (x)
    {
        int t=x&1;
        if (t) y++;
        else l++;
        x>>=1; 
    }
    if (l>=y) return true;
    return false; 
}
 
int getans(int x)
{
    if (!x) return 0;
    int p=x/1000000;
    int re=ans[p];
    for (int i=p*1000000+1;i<=x;i++) if (check(i)) re++;
    return re;
}
 
